#include <util/Bail.hpp>

/******************************************************************************/

#include <stdexcept>

/******************************************************************************/

namespace {

class Bail : public std::runtime_error {
public:
	explicit
	Bail(std::string const & msg)
		: std::runtime_error(msg)
	{
	}
};

}

/******************************************************************************/

namespace lsp {
namespace util {

/******************************************************************************/

void bail(std::string const & msg) {
	throw Bail(msg);
}

/******************************************************************************/

}
}

