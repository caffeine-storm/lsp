#include <io/StreamSource.hpp>

/******************************************************************************/

#include <string>

/******************************************************************************/

namespace lsp {
namespace io {

/******************************************************************************/


StreamSource::StreamSource( std::istream & in )
	: src_( in )
{
}

/******************************************************************************/

char StreamSource::next_() {
	return src_.get();
}

/******************************************************************************/

char StreamSource::peek_() const {
	return src_.peek();
}

/******************************************************************************/

bool StreamSource::has_more_() const {
	return src_.peek() == std::char_traits<char>::eof();
}

/******************************************************************************/

}
}

